# 链接/商品ID识别（解决链接类未分类）
PRODUCT_PATTERNS = {
    'jd_item': r'item\.jd\.com/(\d+)',
    'jd_order': r'订单号[:：]\s*(\d+)',
    'product_id': r'100\d{9}',  # 京东商品ID格式
}

def normalize_text(text: str) -> str:
    """
    文本归一化（处理口语化缩写）
    """
    text = text.lower()
    for short, full in COLLOQUIAL_MAPPINGS.items():
        text = text.replace(short, full)
    return text

def extract_product_id(text: str) -> str:
    """
    从文本中提取商品ID
    """
    import re
    for pattern_name, pattern in PRODUCT_PATTERNS.items():
        match = re.search(pattern, text)
        if match:
            return match.group(1)
    return None

def classify_with_extended_keywords(text: str) -> list:
    """
    使用扩展关键词库进行分类
    
    Returns:
        list: 匹配的分类列表（可能多个）
    """
    text = normalize_text(text)
    matched_categories = []
    
    for category, keywords in KEYWORD_PATTERNS_EXTENDED.items():
        for keyword in keywords:
            if keyword in text:
                matched_categories.append(category)
                break
    
    # 检查是否包含商品链接
    if extract_product_id(text):
        if '02-产品咨询' not in matched_categories:
            matched_categories.append('02-产品咨询')
    
    return matched_categories

